public class hour {
    public void hourtomin(int hr)
    {
      double h1=hr*60;
      System.out.println("Hour to minute:"+h1);
      
    }
    public void hourtosec(int hr){
        double h2=hr*3600;
        System.out.println("Hour to sec:"+h2);
    }
    
}
